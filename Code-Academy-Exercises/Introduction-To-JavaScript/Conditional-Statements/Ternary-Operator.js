let isLocked = false;

isLocked ? console.log('You will need a key to open the door.') : console.log('You will not need a key to open the door.');

let isCorrect = true;

isCorrect ? console.log('Correct!') : console.log('Incorrect!');

let favoritePhrase = 'Love That!';

favoritePhrase === 'Love That!' ? console.log('I love that!') : console.log("I don't love that!");

//You can use a ternary operator to simplify a if...else statement. A ? is used after the variable and the first console.log is printed if the condition evaluates to true and the the second console.log is printed if the condition evalutes to be false.
